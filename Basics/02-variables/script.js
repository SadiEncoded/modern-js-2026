// Variables: var, let, const
// Variable declaration, assignment, and reassignment

let message; // Variable declaration
message = "Hello, World!"; // Variable assignment
message = "Hello, JavaScript!"; // Reassigning a new value

console.log(message); // Output the value of the variable


// var usage
var name = "Sadi";

console.log(name);

// let usage
let age = 23;

console.log(age);

// const usage
const pi = 3.1416;

console.log(pi);

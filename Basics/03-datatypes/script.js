//datatypes in javascript

// 1. String
let name = "John Doe";
console.log("name datatype:", typeof(name)); // Output: string

// 2. Number
let age = 30;
console.log("age datatype:", typeof(age)); // Output: number

// 3. Boolean
let isStudent = true;
console.log("isStudent datatype:", typeof (isStudent)); // Output: boolean

// 4. Undefined
let address;
console.log("address datatype:", typeof (address)); // Output: undefined

// 5. Null
let phoneNumber = null;
console.log("phoneNumber datatype:", typeof (phoneNumber)); // Output: object
